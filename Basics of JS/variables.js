//JS variables can be declaed in four ways: automatically , var , let , const;


//declaring variable automatically
x=10;
y=20;
z=x+y;
console.log(z);


//declaring variable using var keyword
var a=1;
var b=20;
var c=a+b;
console.log(c);

//declaring variable using let keyword
let d=1;
let e=2;
let f=d+e;
console.log(f);


//declaring variable using const keyword
const p=0;
const q=0;
console.log(p+q);

//declaring variable using these mixed keyword
const num1=4;
const num2=6;
let sum=num1+num2;
console.log("Sum is:"+sum);